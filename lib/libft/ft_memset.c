#include "libft.h"

void	*ft_memset(void *str, int c, size_t len)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char *)str;
	while (i < len)
	{
		a[i] = c;
		i++;
	}
	return (a);
}
